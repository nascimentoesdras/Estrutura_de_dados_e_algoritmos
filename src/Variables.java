public class Variables {
    public static void main(String[] args) {
        /*
        * In Java, to declaring variables we have to start declaring the data type
        * that is required to our necessities.
        *
        * They must be String, char, int, float or boolean.
        *
        * Note that the String type starts with uppercase because it is treated
        * like a class, and like other classes have your own methods.
        *
        * Let's now write our variables and print her outputs.
        * */

        String myString = "Isto é uma String em Java!";
        char myChar = 'A';
        int myInt = 123;
        float myFloat = 123.4f;
        boolean myBool = true; // or 'false'


        // Saídas de cada tipo:

        System.out.println(myString);
        System.out.println(myChar);
        System.out.println(myInt);
        System.out.println(myFloat);
        System.out.println(myBool);

    }
}
